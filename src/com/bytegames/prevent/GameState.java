package com.bytegames.prevent;


/**
 * @author byte
 *
 * Represents the current state of the game.
 */
public enum GameState {
    /**
     * The current state is in game.
     */
    GAME,
    /**
     * The current state is in main menu.
     */
    MENU,
    /**
     * The current state is in menu to choose the level.
     */
    LEVELMENU,
    /**
     * The current state is the credits screen.
     */
    CREDITS
    
}